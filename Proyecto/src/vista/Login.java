    private void jPasswordField_contraseñaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPasswordField_contraseñaMouseClicked
        if(jPasswordField_contraseña.getText().equals("************")){
            jPasswordField_contraseña.setText("");
            jPasswordField_contraseña.setForeground(new Color(204,204,204));
        }
        if(jTextField_usuario.getText().isEmpty()){
            jTextField_usuario.setText("ingrese su correo");
            jTextField_usuario.setForeground(new Color(102,120,102));
        }
    }//GEN-LAST:event_jPasswordField_contraseñaMouseClicked

