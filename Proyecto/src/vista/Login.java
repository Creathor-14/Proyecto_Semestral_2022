    private void jLabel_AccederMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_AccederMouseClicked
        String usuario = jTextField_usuario.getText();
        String contraseña = jPasswordField_contraseña.getText();
        try {
            boolean existe = new Impl_prueba().login(usuario, contraseña);
            if(existe){
                new Menu().setVisible(true);
                dispose();
            }else{
                 JOptionPane.showMessageDialog(null,"False");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jLabel_AccederMouseClicked

